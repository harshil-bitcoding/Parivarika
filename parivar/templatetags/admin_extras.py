@register.filter(is_safe=True)
def app_icon(app_label):
    """Return inline SVG icon for the given app label (marked safe)."""
    return mark_safe(APP_ICONS.get(str(app_label).lower(), FALLBACK_ICON))


@register.simple_tag
def home_icon():
    """Return inline SVG home icon (marked safe)."""
    return mark_safe(HOME_ICON)
